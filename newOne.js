function checkPalindrome(x){
    let val = x.toString()
    let a = val.split("").reverse().join("")
    
    if(x == a){
        console.log("the string is palindrome")
    }else{
        console.log("the string is not palindrome")
    }
    
}
 


checkPalindrome(12321)


